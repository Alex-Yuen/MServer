-- cost为负数表示不可行走，正数数值表示行走的难度，值越大，行走消耗越大，寻路优先级越低[1,127]
local grid = 
{
    { y = 0,cost = 1,x_min = 1,x_max = 32},
}

for _,range in pairs( grid ) do
    local y = range.y
    local cost = range.cost
    for x = range.x_min,range.x_max do
        if not map_mgr:fill_map(id,x,y,cost) then
            PLOG("fill map error")
        end
    end
end


